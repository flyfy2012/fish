package com.swust.utils;

import java.util.Random;
/**
 * 创建6位随机字符
 * @Description 
 * @author inging44
 * @date 2016年1月13日 上午10:19:19 
 * @version V0.1
 */
public class CreateRandom{
	public static String getCode(){
		Random r = new Random();
		StringBuffer sb = new StringBuffer();
		for(int i=0;i<6;i++){
			sb.append(r.nextInt(9));
		}
		return  new String(sb);
	}
}
